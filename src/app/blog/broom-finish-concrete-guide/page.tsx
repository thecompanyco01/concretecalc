        <p>
          <strong>Cause:</strong> Inconsistent pressure, stopping and starting mid-stroke, or a worn/uneven broom.<br />
          <strong>Fix:</strong> Use a new broom with even bristles. Pull in one continuous stroke from edge to edge. Maintain consistent speed and pressure throughout.
        </p>

